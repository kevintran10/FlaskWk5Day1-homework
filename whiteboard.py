# Write a function that accepts an integer n and a string s as parameters, and returns a string of s repeated exactly n times.
# EXAMPLES:
# solution(6, "I")     -> "IIIIII"
# solution(5, "Hello") -> "HelloHelloHelloHelloHello"

def integerToString(n, s):
    return s * n

print(integerToString(9,"I"))



